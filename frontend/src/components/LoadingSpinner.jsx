import React from 'react'

const LoadingSpinner = ({ fullScreen = false, size = 'md', text = '' }) => {
  const sizeClasses = {
    sm: 'w-4 h-4 border-2',
    md: 'w-8 h-8 border-3',
    lg: 'w-12 h-12 border-4',
    xl: 'w-16 h-16 border-4'
  }

  const container = fullScreen ? 'fixed inset-0 flex items-center justify-center bg-white/80 backdrop-blur-sm z-50' : 'flex items-center justify-center'

  return (
    <div className={container}>
      <div className="flex flex-col items-center space-y-4">
        <div className={`${sizeClasses[size]} border-primary-500 border-t-transparent rounded-full animate-spin`} />
        {text && (
          <p className="text-gray-600 text-sm font-medium animate-pulse">{text}</p>
        )}
      </div>
    </div>
  )
}

export default LoadingSpinner
